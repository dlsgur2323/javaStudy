package e_oop;

public class Calculator {
	
	//산술연산을 수행할 수 있는 메소드를 만들거에요
	// + - * / % 5개의 연산을 수행해주는 5개의 메소드 만들어주시면 됩니다.
	//각 메소드는 2개의 파라미터를 받아서 연산하고 그 결과값을 리턴 해준다.
	
	double sum(double p,double p2){
		return p+p2;
	}
	double sub(double p,double p2){
		return p - p2;
	}
	double multi(double p,double p2){
		return p * p2;
	}
	double div(double p,double p2){
		return p/p2;
	}
	double rem(double p,double p2){
		return p%p2;
	}
	
}









